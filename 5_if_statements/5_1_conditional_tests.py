# 5.1 CONDITIONAL TESTS
## EXERCISE 5-1
ages = [20, 25, 27, 30, 31,  35]
print(ages)

print(f"Is {ages[0]} >= 30? I predict False")
print(ages[0] >= 30)

print(f"Is {ages[1]} >= 30? I predict False")
print(ages[1] >= 30)

print(f"Is {ages[2]} = 30? I predict False")
print(ages[2] == 30)

print(f"Is {ages[4]} = 30? I predict False")
print(ages[4] == 30)

print(f"Is {ages[5]} > 35? I predict False")
print(ages[5] > 35)

print(f"Is {ages[0]} <= 30? I predict True")
print(ages[0] <= 30)

print(f"Is {ages[1]} <= 30? I predict True")
print(ages[1] <= 30)

print(f"Is {ages[2]} not equal to 30? I predict True")
print(ages[2] != 30)

print(f"Is {ages[3]} equal to 30? I predict True")
print(ages[3] == 30)

print(f"Is {ages[4]} not equal to 30? I predict True")
print(ages[4] != 30)
