# Stripping whitespace

favorite_language = "Python "
print(favorite_language)

# Remove TEMPORARILY whitespace at the right end of a string
favorite_language.rstrip()

# Remove PERMANENTLY whitespace at the right end of a string
favorite_language = favorite_language.rstrip()

# Remove TEMPORARILY whitespace at the left end of a string
favorite_language.lstrip()

# Remove TEMPORARILY whitespace at both ends of a string
favorite_language.strip()
