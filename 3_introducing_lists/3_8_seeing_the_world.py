# 3.3 ORGANIZING A LIST
## EXERCISE 3-8

cities = ['Fresno', 'Sacramento', 'Oakland', 'Bakersfield', 'Anaheim']
print(cities)

print("\n")
print(sorted(cities))
print(cities)

print("\n")
print(sorted(cities, reverse = True))
print(cities)

print("\n")
print(cities)
print(cities.reverse())
print(cities)

print("\n")
print(cities.reverse())
print(cities)

print("\n")
cities.sort()
print(cities)

print("\n")
cities.sort(reverse = True)
print(cities)
